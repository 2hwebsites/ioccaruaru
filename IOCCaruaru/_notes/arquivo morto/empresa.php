                    serviços diferenciados.
                </p>
                <p class="page-p">
                    Com mais de vinte anos de experiência, o Instituto de Olhos de Caruaru tornou-se referência no atendimento oftalmológico, resultado de
                    muito empenho, investimento e dedicação de todos os colaboradores.
                </p>
                <p class="page-p">
